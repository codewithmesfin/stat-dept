            <p class="text-justify">The core values of the department are the following among others:</p>
            <ul>
              <li>Professionalism</li>
              <li>Academic freedom</li>
              <li>Respect diversity and the university of knowledge</li>
              <li>Accountability</li>
              <li>Excellence in service and delivery (learning-teaching, research and community
              service)</li>
            </ul>
            <p class="h4 text-left text-danger">1.4. Objective of the Department</p>
            <p class="text-justify">
              The principal objective of the department is to train all categories of professionals (lower,
              middle and upper) who will serve in the collection, processing and analysis of statistical
              data. In addition, the department prepares students for teaching/research positions in
              colleges and universities and leadership in statistical services in the public and private
              sectors.
            </p>
            <p class="text-justify">
              On account of the special needs of the nation in this area, the training gives more
              emphasis to application of Statistics to all problems.
            </p>
            <ul class="obj">
              <li>
                To train statisticians at the B.Sc, M.Sc and PhD levels who will be able to take-up
                positions in the public and private sectors and provide professional support for
                orderly development of the country.
              </li>
              <li>To carry out research in theoretical and applied statistics</li>
              <li>
                To provide consultancy services and run short term courses in statistics and
                related fields.
              </li>
              <li>
                To participate and play an active role in the activities of professional statistical
                and related societies.
              </li>
            </ul><hr>
