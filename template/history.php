            <p class="text-justify">
              The minimum number of credits that a graduate student must pass is 28 for Plan A, and 34 for Plan
              B.
              In addition, a student in Plan A must undertake a 6-credit thesis research, submit the thesis to the
              department and score a pass mark in the oral defence of the thesis, whereas a student in plan B must
              work on a 3-credit research project dealing with the application of statistical methods on real or
              simulated data, write the report and submit it to the department.
            </p>
            <p class="text-justify">
              The cumulative grade points required for graduation for both Plan A and Plan B students is as
              stated in the general university requirements.
            </p>
            <p class="text-justify">The duration of study is</p>
            <p class="text-center">
              two years for full-time students <br>
              and <br>
              four years for part-time students.
            </p>
            <p class="h4 text-left text-danger">4.5. Quality Assurance</p>
            <p class="text-justify">
              In order to assure the quality of the Statistics MSc program every effort will be made to make
              appropriate books and journals available to students and course lectures are delivered by a staff
              member with a rank of assistant professor and above. Moreover, the teaching facilities and
              materials will be reviewed on a regular basis. External audit of the performance of the
              Department by professionals will also be made.
            </p>
            <p class="text-info h3 my-3">5. The PhD Program</p>
            <p class="text-justify">
              The PhD program of the Department of Statistics was launched in 2012 and was approved by the
              Senate of AAU in April 2013. Although there are two options (a course-based dissertation and a
              dissertation-only option) the program started with the latter option.
            </p>
            <p class="h4 text-left text-danger">5.1. Objective</p>
            <p class="text-justify">The objective of the Ph. D. program is to produce highly-qualified statisticians who;</p>
            <ul>
              <li>
                Can initiate and carry out independent and original research to further knowledge in their
                respective fields of specialization and utilization of that knowledge for solving current real
                life problems
              </li>
              <li>
                Are capable of taking positions in universities, industry, government ministries and
                agencies, research institutes, and financial institutions.
              </li>
            </ul>
            <p class="h4 text-left text-danger">5.2. Concentration Area</p>
            <p class="text-justify">
              The Doctoral program in Statistics prepares students for careers in research, applications, and
              teaching. Students choose one of three areas of concentration for their course work:
            </p>
            <ul>
              <li>Biostatistics</li>
              <li>Mathematical Statistics</li>
              <li>Applied Statistics</li>
            </ul>
            <p class="text-justify">
              <strong>Biostatistics</strong> emphasizes an exposure to various traditional topics as well as recent advances in
              biostatistics and in statistics. It prepares students for methodologic and applied research positions
              in academia and/or for consulting and other positions in industry. The goal is to enhance a
              student's ability to develop new methodologies as well as to address applied questions that arise
              in the medical, agricultural and biological sciences.
            </p>
            <p class="text-justify">
              <strong>Mathematical Statistics</strong> emphasizes a broad and solid foundation in the techniques and
              underpinnings of mathematical statistics. It emphasizes both breadth and depth of training and is
              intended to produce well-rounded, knowledgeable scholars. This area of concentration is an
              excellent
              preparation
              for
              both
              academic
              positions
              in
              mathematical
              statistics
              and
              industrial/government positions that require broadly trained statisticians with a strong
              understanding of statistical theory.
            </p>
            <p class="text-justify">
              <strong>Applied Statistics</strong> emphasizes a high degree of mastery in research design, theory, methodology
              and execution of a broad array of statistical models, such as linear, nonlinear, categorical, spatial,
              correlated response, nonparametric, multivariate, time series, etc. This area of concentration
              prepares students to specify and choose appropriate models; fit models using available statistical
              software; and make sound statistical conclusions and interpretive statements. It also prepares
              students to communicate and present research findings in the related field of application in a
              clear and understandable manner. The students in this category may be required to participate in
              interdisciplinary field coursework related to their application interest such as business
              environmental science, management science, industrial engineering, finance, and other areas.
            </p>
            <p class="h4 text-left text-danger">5.3. Graduate Profile</p>
            <p class="text-justify">Graduates of the Ph. D. program would be able to:</p>
            <ul>
              <li>
                Initiate and conduct independent and original research to further knowledge in their
                respective fields of specialization and utilization of that knowledge for solving current
                real life problems.
              </li>
              <li>
                Engage in the teaching of statistics at undergraduate and postgraduate levels of
                universities and colleges thereby contributing effectively in the production of high
                quality graduates.
              </li>
              <li>
                Direct and supervise research activities related to their fields of specialization at
                postgraduate levels and various research institutions.
              </li>
              <li>
                Play leading roles in statistical activities carried out in relevant authorities, agencies,
                and major statistical units in various organizations.
              </li>
              <li>
                Play leading roles in providing relevant statistical information, data-base and quality
                service for decision makers, researchers and development programs.
              </li>
              <li>
                Attain experience in communicating statistical methods and results to fellow
                statisticians and non-statisticians.
              </li>
              <li>
                Attain deep knowledge in their respective fields of specialization and broad-base
                knowledge of modern statistical methods, including statistical computing.
              </li>
              <li>
                Disseminate findings from original methodologic or applied research through high
                quality peer reviewed publications or books.
              </li>
            </ul>
