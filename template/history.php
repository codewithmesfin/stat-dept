                Education is conducted with the highest degree of excellence.</li>
                <li>To provide all undergraduate students with an education in Statistics that will
                  serve as part of foundation for long-life learning
                </li>
                <li>To provide postgraduate students with an education in statistics at higher level
                  to alleviate the shortage in skilled manpower in the various sectors of the
                  economy.
                </li>
                <li>To develop fundamental statistical theory and methodology.</li>
                <li>
                  To establish an atmosphere of creative endeavor that supports
                  interdisciplinary collaborations, innovative projects, interesting courses and
                  informal discussion that mutually benefit students and faculty.
                </li>
                <li>
                  To be actively involved in statistical problems that arise in such diverse field
                  as Molecular Biology, Geophysics, Astronomy, AIDS research, Sociology,
                  Political Science, Education, Demography, National Survey and Censuses,
                  Economics, Environmental Science and Metrology.
                </li>
                <li>
                  To offer statistical consultancy services to the university community (students
                  and staffs researchers), governments at all levels, private businesses and
                  industries.
                </li>
                <li>To train Statistics educators for the emerging Universities in the country.</li>
                <li>
                  To nurture and stimulate the discipline and profession of statistics through
                  publication and staff participation in major local and international
                  conferences.
                </li>
                <li>To create and maintain flourishing staff-students relationship.</li>
            </ol>
