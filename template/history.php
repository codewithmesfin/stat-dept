            <p class="h4 text-left text-danger">5.5. Program of Study</p>
            <p class="h5">General Provision</p>
            <p class="text-justify">
              The Ph. D. program has two options. The Departmental Graduate Committee (DGC) shall place
              a candidate to one of the options based on critical assessment of academic records, teaching and
              research experience of the candidate.
            </p>
            <strong>
              <p class="mx-3">Option I: course work and research.</p>
              <p class="mx-3">Option II: research only.</p>
            </strong>
            <p class="h4 text-left text-danger">5.6. Doctoral Dissertation</p>
            <p class="h5">a. General Requirements</p>
            <p class="text-justify">
              All candidates will be expected to carry out original research in their respective fields of
              specialization, the results of which will be presented as a doctoral dissertation subject to
              careful scrutiny by examiners.
            </p>
            <p class="h6">Option I</p>
            <ol class="mission">
              <li>
                A candidate will be required to pass all course requirements and get approval for their
                research topic before being eligible to conduct research towards his/her doctoral
                dissertation.
              </li>
              <li>
                The doctoral dissertation shall constitute the partial fulfillment of the requirements to
                the Ph. D. degree program.
              </li>
            </ol>
            <p class="h6">Option II</p>
            <ol class="mission">
              <li>
                A candidate will be required to get approval of his/her doctoral research topic
                proposal as described in Part 6 above before being eligible to conduct research
                towards the doctoral dissertation.
              </li>
              <li>
                The doctoral dissertation shall constitute the fulfillment of the requirement to the Ph.
                D. degree program.
              </li>
            </ol>
            <p class="h5">b. Format of Doctoral Dissertation</p>
            <p class="text-justify">
              The doctoral dissertation format shall be in accordance with the guidelines set by the
              Office of Graduate Studies and Research Programs.
            </p>
            <p class="h5">c. Submission of Doctoral Dissertation</p>
            <ol class="mission">
              <li>
                No candidate may be permitted to submit a doctoral dissertation in less than two
                academic years from the date of first registration of dissertation except with a
                special permission of the University Senate.
              </li>
              <li>
                A doctoral dissertation must have an abstract and the abstract should not be more
                than 500 words. It must also include five to seven key words.
              </li>
            </ol>
            <p class="h5">d. Procedures for submitting and Defending the Dissertation</p>
            <p class="text-justify">
              When a candidate, after conferring with the supervisor, gives notice of readiness to
              submit his dissertation, the College Graduate Committee (CGC) appoints an Examination
              Committee upon the recommendation of the DGC that finds the dissertation acceptable.
              The Examination Committee shall constitute of at least four members including the
              Advisor, the Chairperson, and two examiners with the rank of at least Associate Professor
              in Statistics.
            </p>
            <p class="text-justify">
              The doctoral dissertation defense shall be scheduled within six months after the
              submission of the doctoral dissertation to the DGC.
            </p>
            <p class="text-justify">
              Appropriate number of copies of the doctoral dissertation must be submitted to
              the
              Department and be available for members of the Committee.
            </p>
            <p class="text-justify">
              The candidate shall defend his/her doctoral dissertation Examination to the duly
              appointed Examination Committee.
            </p>
            <p class="h5">Doctoral Dissertation Defense</p>
            <p class="h6">i. Accepted doctoral dissertation</p>
            <p class="text-justify">A doctoral dissertation is accepted</p>
            <ol>
              <li>With no change or with some minor changes.</li>
              <li>With modification.</li>
            </ol>
            <p class="text-justify">
              Doctoral dissertation might require substantial changes in substance, which are to be
              made to the satisfaction of members of the Examination Committee or its delegate. The
              report of the Examination Committee must include an outline of the nature of the changes
              required and must indicate the time by which the changes are to be completed.
            </p>
            <p class="h6">ii. Rejected doctoral dissertation</p>
            <p class="text-justify">A doctoral dissertation will be rejected if:</p>
            <ol>
              <li>
                The work does not meet the required standard as judged by the Examination
                Committee and is not approved by the DGC.
                </li>
                <li>
                  The work is plagiarized as judged by the Examination Committee not
                  approved by the DGC.
                </li>
                <li>
                  it has been already used to obtain a degree from AAU or another University
                  unless the Examination Committee is convinced that enough work has been
                  done to expand the scope and depth of the subject.
                </li>
            </ol>
            <p class="h6">iii. Grade</p>
            <p class="text-justify">
              After the candidate's presentation and defense, the examination board shall grade
              the work, preferably through consensus as accepted (pass) or rejected (fail). An
              accepted dissertation shall be rated as satisfactory, good, very good, or excellent.
            </p>
            <p class="h4 text-danger">5.7. Graduate Requirements</p>
            <p class="h6">Option I</p>
            <ul>
              <li>
                A minimum of 17 credit hours with CGPA greater than or equal to 3.00 as described in
                Part 5.6 above.
              </li>
              <li>A minimum grade of "B" in each course</li>
              <li>Accepted dissertation as described in Part 5.6 above.</li>
            </ul>
            <p class="h6">Option II</p>
            <ul>
              <li>Accepted dissertation as described in Part 5 above.</li>
            </ul>
