            <p class="text-info h3 my-3">3. The BSc Program</p>
            <p class="text-justify">
              The BSc program in Statistics started in 1973 as a four year program. Due to changes
              in government policy, the duration of the study has been reduced to three years in
              2010.
            </p>
            <p class="h4 text-left text-danger">3.1. Objectives of the B.Sc program</p>
            <p class="text-justify">
              The major objective of the B.Sc degree program is to standardize courses in line with the
              current development of the science and global education of statistics and produce all
              rounded middle-level statisticians who can render a professional service in different
              social and economic sectors. Specifically, the objective of the B.Sc program is the
              training of statistics professionals who can;
            </p>
            <ul class="obj">
              <li>Serve in the collection, processing and analysis of statistical data.</li>
              <li>
                work independently and supervise the day to day management of major
                statistical units or offices
              </li>
              <li>
                Assist in statistical research and the provision of statistical services in the
                public and private sectors.
              </li>
            </ul>
            <p class="h4 text-left text-danger">3.2. Graduate Profile</p>
            <p class="text-justify">A B.Sc. graduate in statistics would;</p>
            <ol class="mission">
              <li>
                have a comprehensive knowledge of study designs, types of data and modern
                methods of statistical analysis of data collected in a wide range of fields of
                application such as economics, health, agriculture, industry, business,
                environment
              </li>
              <li>
                manage, coordinate and supervise the day-to-day management of major statistical
                units or offices
              </li>
              <li>
                assist in statistical research and the provision of statistical services in research
                establishments, business and government and non-government agencies
              </li>
              <li>plan surveys and experimental designs</li>
              <li>
                use statistical soft wares and/or write programs for the analysis and management
                of data as well as give trainings on the usage and applications of statistical soft
                wares.
              </li>
              <li>write clear statistical reports and properly communicate the results</li>
              <li>maintain professional ethics,</li>
            </ol>
            <p class="h4 text-left text-danger">3.3. Admission Requirement</p>
            <p class="text-justify">
              In addition to the Addis Ababa University’s general admission requirement an applicant
              must have successfully completed the preparatory [10+2] program in a recognized school
              with a minimum of “C” grade or equivalent in mathematics in the national or equivalent
              placement/ entrance examination or the applicant must have graduated from a recognized
              university/ college with a diploma in statistics.
            </p>
            <p class="text-info h3 my-3">4. The MSc program</p>
            <p class="text-justify">
              The MSc program of the Department of Statistics was launched in 1983, and since then it has
              been supplying statistics professionals who can manage the day-to-day activities of major
              statistical units and serve as senior statisticians in various organizations.
            </p>
            <p class="h4 text-left text-danger">4.1. Objectives of the MSc program</p>
            <p class="text-justify">
              The main goal of the MSc program is to advance the mission of the Department through
              achieving excellence in our three core functions: teaching, research, and providing service to the
              wider community.
            </p>
            <p class="text-justify">
              he foremost goal of the program is to train middle level statisticians in order to meet the
              demands of the socioeconomic, business and development sectors in the country.
            </p>
            <p class="text-justify">
              On account of the needs of the nation the program gives more emphasis on applications of
              statistics. The specific goals/objectives of the training are:
            </p>
            <ul>
              <li>
                To up-grade the professional competence of junior statisticians;
              </li>
              <li>
                To prepare statistics professionals that will provide leadership in statistical services
                in the public and private sectors;
              </li>
              <li>
                To train middle level statisticians who can assist in statistical research undertakings
                and provision of statistical services.
              </li>
              <li>
                To train university instructors and to prepare the same for subsequent doctoral
                training in Statistics.
              </li>
            </ul>
            <p class="text-justify">The M.Sc. program is structured into two streams:</p>
            <ol>
              <li>Applied Statistics</li>
              <li>Biostatistics</li>
            </ol>
            <p class="text-justify">Each stream has two options:</p>
            <ol >
              <li>Plan A, with thesis</li>
              <li>Plan B, without thesis</li>
            </ol>
            <p class="h4 text-left text-danger">4.2. Admission Requirement</p>
            <p class="text-justify">In order to be admitted to the M.Sc. program in a specific stream</p>
            <ul>
              <li>
                the applicant must have a bachelor's degree in statistics or any other field related to the
                stream from a recognized university
              </li>
              <li>
                the performance of the applicant in the M.Sc. entrance examination must be satisfactory
                as assessed and decided by the DGC
              </li>
              <li>
                The applicant must also meet the general admission requirements of the Addis Ababa
                University.
              </li>
            </ul>
            <p class="h4 text-left text-danger">4.3. Graduate Profile</p>
            <p class="text-justify">
              An MSc graduate in Statistics would
              have a thorough knowledge of study designs and types of data collected in a wide range of
              fields of application
            </p>
            <ol class="mission">
              <li>
                have a thorough knowledge of the familiar methods of statistical analysis used in
                scientific research, social and economic sectors
              </li>
              <li>plan study designs, collection, processing, and analysis of statistical data</li>
              <li>
                apply both simple and advanced methods of statistical analysis on data sets arising in a
                wide range of fields of application
              </li>
              <li>manage the day-to-day activities of major statistical units</li>
              <li>use various computer packages for data entry and statistical analysis</li>
              <li>Write clear reports, research papers, etc. based on statistical analysis.</li>
            </ol>
