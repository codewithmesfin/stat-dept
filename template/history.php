          <p class="text-justify">
              In the late 1950s, as the duties and responsibilities of the central government began to
              expand and especially as the Ethiopian government started to prepare a series of five year
              development plans, the need to collect, summarize and analyze empirical data became
              obvious. The shortage of junior (certificate), medium (diploma) and senior (degree) level
              professionals in the field of statistics became more acute. This was also true for the rest
              of the continent. To alleviate this shortage, based on the recommendations of the first
              African Statisticians Conference held in 1959, the UN established “International
              Statistical Training Centers” as regional hubs. The training center for the East African
              Region was opened in 1961 in the Faculty of Science of the former Haile Sellassie I
              University. This center was named “UN International Statistical Training Center”. It used
              to provide a one-year program leading to a certificate for candidates sponsored by
              different governments of the region. It operated from 1961 to 1966, and the Department
              was provided with funding and technical assistance by the UN. During this period the
              enrolment of students from Ethiopia, Egypt, Libya, Sudan, Malawi, Zambia, Basutoland,
              and Somalia were 52, 24, 6, 34, 10, 4, 3, and 4 respectively.
            </p>
            <p class="text-justify">
              In 1966, to alleviate the shortage of medium and high level statisticians, the UN helped in
              the establishment of additional two programs: a three-year diploma program and a four-
              year degree program. The first intake of the degree and diploma programs was the
              freshman class of 1966. Besides the regular program, certificate and diploma programs
              were also introduced in the extension division.
            </p>
            <p class="text-justify">
              One of the most interesting features of these programs was their flexibility. Students who
              performed well in the certificate program could continue to the diploma level. The same
              was true for transfer from diploma to degree program. The reverse was also true i.e.,
              students who performed poorly were advised to join a lower level program. In 1973, the
              Statistical Training Center was upgraded to the Department of Statistics and it has been
              running the degree program for regular students and the diploma program for extension
              students since then. In 1974, the certificate program was terminated. In 1975 the diploma
              program was confined to the extension division only.
            </p>
            <p class="text-justify">The Department launched MSc program in 1983 and a PhD program in 2012/13.</p>
